        Informasi. Tiga tahun kemudian, tepatnya pada tanggal 13 Juli 1984, ATK
        mendapat status Terdaftar dan berubah menjadi Akademi Manajemen
        Informatika dan Komputer (AMIK) Jakarta. Kemudian pada tanggal 1 Juli
        1985, dibuka jurusan Komputerisasi Akuntansi, dan pada tanggal 21
        September 1985, AMIK Jakarta berganti nama menjadi AMIK BINA
        NUSANTARA&nbsp; <br />
        &nbsp;<br />
        Dalam usia mudanya, sebuah prestasi emas ditoreh AMIK BINA NUSANTARA
        dengan terpilih sebagai Akademi Komputer Terbaik oleh Depdikbud melalui
        Kopertis Wilayah III Jakarta pada tanggal 17 Maret 1986.&nbsp; <br />
        &nbsp;<br />
        Berkat makin meningkatnya kebutuhan masyarakat akan tenaga-tenaga andal
        dalam bidang teknologi informasi, pada tanggal 1 Juli 1986, Sekolah
        Tinggi Manajemen Informatika dan Komputer (STMIK) BINA NUSANTARA
        didirikan dengan Program Strata-1 (S1) jurusan Manajemen Informatika dan
        Teknik Informatika. Bersamaan dengan itu juga dibuka jurusan Teknik
        Komputer (S1).&nbsp; <br />
        &nbsp;<br />
        Pada tanggal 9 November 1987, AMIK BINA NUSANTARA dilebur ke dalam STMIK
        BINA NUSANTARA sehingga terbentuk sebuah lembaga yang menyelenggarakan
        Program Diploma III (DIII) dan Strata-1 (S1). STMIK BINA NUSANTARA
        berhasil memperoleh status "Disamakan" untuk semua jurusan dan jenjang
        pada tanggal 18 Maret 1992, dan pada tanggal 10 Mei 1993 mendapat
        kepercayaan untuk membuka Program Magister Manajemen Sistem Informasi,
        salah satu Program Pascasarjana pertama di Indonesia di bidang
        tersebut.&nbsp; <br />
        &nbsp;<br />
